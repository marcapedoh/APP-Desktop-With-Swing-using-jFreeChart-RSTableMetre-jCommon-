    private void rSMaterialButtonRectangleModifierActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_rSMaterialButtonRectangleModifierActionPerformed
        // TODO add your handling code here:
         Commande cdd1=new Commande();
        if(jCTextFieldidCommande.getText().length()>0){
            cdd1.setId_commande(parseInt(jCTextFieldidCommande.getText()));
        }
        if(jCTextFieldRefCommande.getText().length()>0){
            cdd1.setRef_commande(jCTextFieldRefCommande.getText());
        }
        if(jCTextFieldQuantiteProduit.getText().length()>0){
            cdd1.setQte_prod(parseInt(jCTextFieldQuantiteProduit.getText()));
        }
        if(jCTextFieldMontant.getText().length()>0){
            cdd1.setMontant(parseInt(jCTextFieldMontant.getText()));
        }
        if(jCTextFieldDateCommande.getText().length()>0){
            cdd1.setDate_commande(jCTextFieldDateCommande.getText());
        }
        if(jCTextField1.getText().length()>0){
            cdd1.setDate_recu_commande(jCTextField1.getText());
        }
        int resultat=ServiceCommande.UpdateCommande(cdd1);
        if(resultat==0){
            JOptionPane.showMessageDialog(rootPane,"Erreur de modification un probleme est surevenu!");
        }else{
            JOptionPane.showMessageDialog(rootPane,"Modification effectué avec succés");
        }
        
    }//GEN-LAST:event_rSMaterialButtonRectangleModifierActionPerformed

    private void rSMaterialButtonRectangleSupprimerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_rSMaterialButtonRectangleSupprimerActionPerformed
        // TODO add your handling code here:
         if(jCTextFieldRecherche.getText().length()>0){
            int retour1=JOptionPane.showConfirmDialog(rootPane,"Voulez-vous réellement supprimer cet commande?","Confirmation Message",JOptionPane.YES_NO_OPTION);
            if(retour1==0){
                int retour =ServiceCommande.DeleteCommande(jCTextFieldRecherche.getText());
                if(retour==0){
                    JOptionPane.showMessageDialog(rootPane,"Erreur sql revoir les parametres de la methode delete et l'execution","Warning message",JOptionPane.ERROR_MESSAGE);
                }else{
                    JOptionPane.showMessageDialog(rootPane,"Commande supprimer!","Information Message",JOptionPane.INFORMATION_MESSAGE);
                    jCTextFieldRecherche.setText(null);
                    jCTextFieldRefCommande.setText(null);
                    jCTextFieldQuantiteProduit.setText(null);
                    jCTextFieldMontant.setText(null);
                    jCTextFieldDateCommande.setText(null);
                    jCTextField1.setText(null);
                    jCTextFieldRecherche.requestFocus();
                    
                }
            }    
        }else{
            JOptionPane.showMessageDialog(rootPane, "vous ne pouvez pas rechercher quelque chose que vous n'avez pas demander autrement dit veuillez fournir les le nom pour la recherche du client","Information Message",JOptionPane.INFORMATION_MESSAGE);
        }
        
    }//GEN-LAST:event_rSMaterialButtonRectangleSupprimerActionPerformed

