    private void rSMaterialButtonRectangleRechercheActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_rSMaterialButtonRectangleRechercheActionPerformed
        // TODO add your handling code here:
        if(jCTextFieldRecherche.getText().length()>0){
           Commande cde=ServiceCommande.ReadCommande(jCTextFieldRecherche.getText());
           if(cde.getId_commande()==0){
               JOptionPane.showMessageDialog(rootPane,"Commande non trouvé","information message", JOptionPane.INFORMATION_MESSAGE);
           }else{
               jCTextFieldidCommande.setText(String.valueOf(cde.getId_commande()));
               jCTextFieldRefCommande.setText(cde.getRef_commande());
               jCTextFieldQuantiteProduit.setText(String.valueOf(cde.getQte_prod()));
               jCTextFieldMontant.setText(String.valueOf(cde.getMontant()));
               jCTextFieldDateCommande.setText(cde.getDate_commande());
               
               jCTextField1.setText(cde.getDate_recu_commande());
           }
        }else{
            JOptionPane.showMessageDialog(rootPane,"Le champs de recherche ne peut pas être nul si vous chercher les informations sur un Client.","Error Message",JOptionPane.ERROR_MESSAGE);
            jCTextFieldRecherche.requestFocus();
        }
    }//GEN-LAST:event_rSMaterialButtonRectangleRechercheActionPerformed

