    private void rSMaterialButtonRectangleSupprimerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_rSMaterialButtonRectangleSupprimerActionPerformed
        // TODO add your handling code here:
        if(jCTextFieldRecherche.getText().length()>0){
            int choix=JOptionPane.showConfirmDialog(rootPane,"Voulez-vous vraiment supprimer ce produit?","COnfirmation Message",JOptionPane.YES_NO_OPTION);
            if(choix==0){
                int prod2=ServiceProduit.DeleteProduit(jCTextFieldRecherche.getText());
                if(prod2==0){
                    JOptionPane.showMessageDialog(rootPane,"Erreur sql revoir les parametres de la methode delete et l'execution","Warning messagr",JOptionPane.ERROR_MESSAGE);
                }else{
                    JOptionPane.showMessageDialog(rootPane,"Produit supprimer!","Information Message",JOptionPane.INFORMATION_MESSAGE);
                    jCTextFieldIdproduit.setText(null);
                    jCTextFieldRecherche.setText(null);
                    jCTextFieldRecherche.requestFocus();
                    jCTextFieldNomProduit.setText(null);
                    jCTextFieldQuantite.setText(null);
                }
            }
        }
    }//GEN-LAST:event_rSMaterialButtonRectangleSupprimerActionPerformed

