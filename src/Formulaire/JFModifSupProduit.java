    private void rSMaterialButtonRectangleModifierActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_rSMaterialButtonRectangleModifierActionPerformed
        // TODO add your handling code here:
        Produit prod1=new Produit();
        if(jCTextFieldIdproduit.getText().length()>0){
            prod1.setId_prod(parseInt(jCTextFieldIdproduit.getText()));
        }
        if(jCTextFieldNomProduit.getText().length()>0){
            prod1.setNom_prod(jCTextFieldNomProduit.getText());
        }
        if(jCTextFieldQuantite.getText().length()>0){
            prod1.setQte_paye(parseInt(jCTextFieldQuantite.getText()));
        }
        int resultat=ServiceProduit.UpdateProduit(prod1);
        if(resultat==0){
            JOptionPane.showMessageDialog(rootPane,"Erreur de modification un probleme est surevenu!");
        }else{
            JOptionPane.showMessageDialog(rootPane,"Modification effectué avec succés");
        }
        
    }//GEN-LAST:event_rSMaterialButtonRectangleModifierActionPerformed

    private void rSMaterialButtonRectangleRechercheActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_rSMaterialButtonRectangleRechercheActionPerformed
        // TODO add your handling code here:
        if(jCTextFieldRecherche.getText().length()>0){
            Produit ILveUBtia=ServiceProduit.ReadProduit(jCTextFieldRecherche.getText());
            if(ILveUBtia.getId_prod()==0){
                JOptionPane.showMessageDialog(rootPane,"Produit non trouvé revoyez le nom du produit", "Information Message", JOptionPane.INFORMATION_MESSAGE);
            }else{
                jCTextFieldIdproduit.setText(String.valueOf(ILveUBtia.getId_prod()));
                jCTextFieldNomProduit.setText(ILveUBtia.getNom_prod());
                jCTextFieldQuantite.setText(String.valueOf(ILveUBtia.getQte_paye()));
               
            }
        }else{
            JOptionPane.showMessageDialog(rootPane,"le champs de recherche ne peut op etre nul pour pouvoir faire la recherche d'un produit", "information message", JOptionPane.INFORMATION_MESSAGE);
            jCTextFieldRecherche.requestFocus();
        }
        
        
    }//GEN-LAST:event_rSMaterialButtonRectangleRechercheActionPerformed

